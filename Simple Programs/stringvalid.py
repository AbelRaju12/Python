s=input()
if(s.isalpha()):
    print("String contains only alphabets..")
elif(s.isdigit()):
    print("String contains only numbers..")
elif(s.isalnum()):
    print("String contains alphabets and numbers..")
if(s.islower()):
    print("String is in lowercase..")
if(s.isupper()):
    print("String is in uppercase..")
